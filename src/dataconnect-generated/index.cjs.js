const { queryRef, executeQuery, mutationRef, executeMutation, validateArgs } = require('firebase/data-connect');

const connectorConfig = {
  connector: 'example',
  service: 'yogesh-protfolio',
  location: 'us-east4'
};
exports.connectorConfig = connectorConfig;

